char	*ft_strrchr(const char *s, int c)
{
	char	*r;
	char	*p;

	p = (char *) s;
	r = 0;
	while (*p != 0)
	{
		if (*p == c % 256)
			r = p;
		p++;
	}
	if (*p == c)
	{
		return (p);
	}
	return (r);
}
